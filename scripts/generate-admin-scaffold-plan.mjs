import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const previewPath = path.join(repoRoot, "resources", "generated", "admin-codegen-preview.json");
const generatedDir = path.join(repoRoot, "resources", "generated");
const generatedPath = path.join(generatedDir, "admin-scaffold-plan.json");
const scaffoldRoot = "resources/generated/scaffold";
const generatedMarker = "Code generated by platform-go admin scaffold plan. DO NOT EDIT.";

const preview = JSON.parse(fs.readFileSync(previewPath, "utf8"));
const resources = [...(preview.resources ?? [])].sort((a, b) => a.resource.localeCompare(b.resource));

function toPosix(value) {
  return value.split(path.sep).join("/");
}

function relativePath(...parts) {
  return toPosix(path.join(...parts));
}

function isInsideAllowedRoot(relativeFile, allowedRoot) {
  const root = path.resolve(repoRoot, allowedRoot);
  const target = path.resolve(repoRoot, relativeFile);
  const relative = path.relative(root, target);
  return relative !== "" && !relative.startsWith("..") && !path.isAbsolute(relative);
}

function existingFileHasGeneratedMarker(relativeFile) {
  const absoluteFile = path.join(repoRoot, relativeFile);
  if (!fs.existsSync(absoluteFile)) {
    return false;
  }
  return fs.readFileSync(absoluteFile, "utf8").includes(generatedMarker);
}

function fileExists(relativeFile) {
  return fs.existsSync(path.join(repoRoot, relativeFile));
}

function candidateFile(role, relativeFile, eventualRuntimeTarget) {
  const inAllowedRoot = isInsideAllowedRoot(relativeFile, scaffoldRoot);
  const exists = fileExists(relativeFile);
  const generated = exists && existingFileHasGeneratedMarker(relativeFile);
  const conflict = !inAllowedRoot || (exists && !generated);
  const status = !inAllowedRoot ? "outsideBoundary" : exists ? (generated ? "regeneratable" : "conflict") : "available";
  return {
    role,
    path: relativeFile,
    eventualRuntimeTarget,
    status,
    generated,
    conflict,
  };
}

function resourceCandidateFiles(resource) {
  const base = relativePath(scaffoldRoot, "admin", resource.resource);
  const files = [];
  if (resource.backend.model) {
    files.push(
      candidateFile(
        "gormModelDraft",
        relativePath(base, "backend", `${resource.backend.model}.go.txt`),
        `internal/platform/model/${resource.backend.model}.go`,
      ),
    );
  }
  files.push(
    candidateFile(
      "repositoryDraft",
      relativePath(base, "backend", `${resource.resource}-repository.go.txt`),
      resource.backend.repositoryPackage,
    ),
  );
  files.push(candidateFile("routeDraft", relativePath(base, "backend", `${resource.resource}-routes.go.txt`), resource.backend.routeFile));
  files.push(
    candidateFile(
      "refinePageDraft",
      relativePath(base, "frontend", `${resource.frontend.resourceName}Resource.tsx.txt`),
      resource.frontend.pageComponent,
    ),
  );
  files.push(
    candidateFile(
      "contractTestDraft",
      relativePath(base, "tests", `${resource.resource}-resource-contract.test.go.txt`),
      `internal/platform/adminresource/${resource.resource}_test.go`,
    ),
  );
  return files;
}

function resourcePlan(resource) {
  const sourceWriting = "disabled";
  if (resource.generationLevel !== "templateCandidate") {
    return {
      resource: resource.resource,
      code: resource.code,
      label: resource.label,
      codegenMode: resource.codegenMode,
      generationLevel: resource.generationLevel,
      sourceWriting,
      dryRunStatus: "manualReviewOnly",
      manualReviewReason: "Resource uses custom codegen mode and must be implemented by a capability-specific adapter.",
      candidateFiles: [],
    };
  }
  const candidateFiles = resourceCandidateFiles(resource);
  const conflictCount = candidateFiles.filter((file) => file.conflict).length;
  return {
    resource: resource.resource,
    code: resource.code,
    label: resource.label,
    codegenMode: resource.codegenMode,
    generationLevel: resource.generationLevel,
    sourceWriting,
    dryRunStatus: conflictCount > 0 ? "blockedByConflicts" : "safePreview",
    manualReviewReason: null,
    candidateFiles,
  };
}

const resourcePlans = resources.map(resourcePlan);
const candidateFiles = resourcePlans.flatMap((resource) => resource.candidateFiles);
const conflictFiles = candidateFiles.filter((file) => file.conflict);
const unsafeFiles = candidateFiles.filter((file) => !isInsideAllowedRoot(file.path, scaffoldRoot));

const plan = {
  generatedBy: "scripts/generate-admin-scaffold-plan.mjs",
  source: "resources/generated/admin-codegen-preview.json",
  sourceVersion: preview.sourceVersion,
  mode: {
    dryRun: true,
    sourceWriting: "disabled",
    conflictDetection: "enabled",
    allowedWriteRoots: [`${scaffoldRoot}/`],
    generatedMarker,
  },
  summary: {
    resourceCount: resourcePlans.length,
    candidateResourceCount: resourcePlans.filter((resource) => resource.generationLevel === "templateCandidate").length,
    manualReviewResourceCount: resourcePlans.filter((resource) => resource.generationLevel === "manualReview").length,
    candidateFileCount: candidateFiles.length,
    conflictCount: conflictFiles.length,
    unsafePathCount: unsafeFiles.length,
  },
  guardrails: [
    "This plan is a dry-run artifact; it must not write runtime source files.",
    "Candidate files are allowed only under resources/generated/scaffold/.",
    "Existing files are conflicts unless they contain the generated marker.",
    "Runtime targets are informational and require explicit human-reviewed source-writing work later.",
  ],
  resources: resourcePlans,
};

const output = `${JSON.stringify(plan, null, 2)}\n`;
if (process.argv.includes("--stdout")) {
  process.stdout.write(output);
} else {
  fs.mkdirSync(generatedDir, { recursive: true });
  fs.writeFileSync(generatedPath, output);
  console.log(`Generated ${path.relative(repoRoot, generatedPath)}`);
}
