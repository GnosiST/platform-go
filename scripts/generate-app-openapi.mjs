import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const contractPath = path.join(repoRoot, "resources", "generated", "app-route-contract.json");
const generatedDir = path.join(repoRoot, "resources", "generated");
const generatedPath = path.join(generatedDir, "openapi.app.json");

const contract = JSON.parse(fs.readFileSync(contractPath, "utf8"));
const routes = contract.routes ?? [];
const usedOperationIds = new Set();

function localized(value) {
  if (!value) return "";
  return value.en || value.zh || "";
}

function pascalCase(value) {
  return String(value)
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

function camelCase(...parts) {
  const value = pascalCase(parts.join(" "));
  return value.charAt(0).toLowerCase() + value.slice(1);
}

function openapiPath(routePath) {
  return routePath.replace(/:([A-Za-z0-9_]+)/g, "{$1}");
}

function pathParams(routePath) {
  return Array.from(routePath.matchAll(/:([A-Za-z0-9_]+)/g)).map((match) => match[1]);
}

function pathParameter(name) {
  return {
    name,
    in: "path",
    required: true,
    schema: { type: "string", minLength: 1 },
  };
}

function routeAction(route) {
  const segments = route.path.replace(/^\/api\/app\/?/, "").split("/").filter(Boolean);
  const last = segments.at(-1) ?? "call";
  if (route.method === "GET" && last === "current") return "current";
  if (route.method === "DELETE") return "delete";
  if (route.method === "POST" && last === "logout") return "logout";
  if (route.method === "POST" && last === "login") return "login";
  if (route.method === "POST") return "create";
  if (route.method === "PUT" || route.method === "PATCH") return "update";
  return last;
}

function operationId(route) {
  const segments = route.path.replace(/^\/api\/app\/?/, "").split("/").filter(Boolean);
  const action = routeAction(route);
  const subject = segments.at(0) ?? route.capabilityId;
  const base = camelCase("app", subject, action);
  let candidate = base;
  let index = 2;
  while (usedOperationIds.has(candidate)) {
    candidate = `${base}${index}`;
    index += 1;
  }
  usedOperationIds.add(candidate);
  return candidate;
}

function security(route) {
  return route.auth === "public" ? [] : [{ appBearerAuth: [] }];
}

function apiResponse(schema) {
  return {
    type: "object",
    properties: {
      data: schema,
      error: { $ref: "#/components/schemas/ErrorBody" },
    },
  };
}

function successResponse(description, schema) {
  return {
    description,
    content: {
      "application/json": {
        schema,
      },
    },
  };
}

function errorResponses() {
  return {
    "400": { $ref: "#/components/responses/BadRequest" },
    "401": { $ref: "#/components/responses/Unauthorized" },
    "403": { $ref: "#/components/responses/Forbidden" },
    "500": { $ref: "#/components/responses/InternalError" },
  };
}

function operation(route) {
  const params = pathParams(route.path);
  return {
    operationId: operationId(route),
    tags: [route.capabilityId],
    summary: localized(route.description) || `${route.method} ${route.path}`,
    description: route.description?.zh && route.description?.en ? `${route.description.en}\n\n${route.description.zh}` : localized(route.description),
    security: security(route),
    parameters: params.map(pathParameter),
    responses: {
      "200": successResponse("OK", apiResponse({ type: "object", additionalProperties: true })),
      ...errorResponses(),
    },
    "x-platform-capability": route.capabilityId,
    "x-platform-auth": route.auth,
    ...(route.permission ? { "x-platform-permission": route.permission } : {}),
  };
}

const paths = {};
for (const route of routes) {
  const pathKey = openapiPath(route.path);
  paths[pathKey] = {
    ...(paths[pathKey] ?? {}),
    [route.method.toLowerCase()]: operation(route),
  };
}

const openapi = {
  openapi: "3.1.0",
  info: {
    title: "Platform App API",
    version: contract.sourceVersion ?? "0.1.0",
    description: "Generated from capability Manifest.App.Routes. Do not edit this file manually; update Go capability manifests and regenerate.",
  },
  servers: [{ url: "/" }],
  tags: (contract.capabilities ?? []).map((capability) => ({ name: capability })),
  paths: Object.fromEntries(Object.entries(paths).sort(([left], [right]) => left.localeCompare(right))),
  components: {
    securitySchemes: {
      appBearerAuth: {
        type: "http",
        scheme: "bearer",
        bearerFormat: "JWT",
        description: "App bearer token with tokenType=app.",
      },
    },
    responses: {
      BadRequest: successResponse("Bad request", apiResponse({ nullable: true })),
      Unauthorized: successResponse("Authentication required", apiResponse({ nullable: true })),
      Forbidden: successResponse("Permission denied", apiResponse({ nullable: true })),
      InternalError: successResponse("Internal server error", apiResponse({ nullable: true })),
    },
    schemas: {
      ErrorBody: {
        type: "object",
        properties: {
          code: { type: "string" },
          message: { type: "string" },
        },
      },
    },
  },
  "x-generated-by": "scripts/generate-app-openapi.mjs",
  "x-source": "resources/generated/app-route-contract.json",
  "x-source-mode": contract.sourceMode,
  "x-security-domain": "app",
};

const output = `${JSON.stringify(openapi, null, 2)}\n`;
if (process.argv.includes("--stdout")) {
  process.stdout.write(output);
} else {
  fs.mkdirSync(generatedDir, { recursive: true });
  fs.writeFileSync(generatedPath, output);
  console.log(`Generated ${path.relative(repoRoot, generatedPath)}`);
}
