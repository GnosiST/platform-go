import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const previewPath = path.join(repoRoot, "resources", "generated", "admin-codegen-preview.json");
const planPath = path.join(repoRoot, "resources", "generated", "admin-scaffold-plan.json");
const generatedDir = path.join(repoRoot, "resources", "generated");
const generatedPath = path.join(generatedDir, "admin-scaffold-draft.md");

const preview = JSON.parse(fs.readFileSync(previewPath, "utf8"));
const plan = JSON.parse(fs.readFileSync(planPath, "utf8"));
const resources = [...(preview.resources ?? [])].sort((a, b) => a.resource.localeCompare(b.resource));
const planByResource = new Map((plan.resources ?? []).map((resource) => [resource.resource, resource]));
const relativePlanPath = path.relative(repoRoot, planPath);

function bullet(value) {
  return `- ${value}`;
}

function nullable(value) {
  return value === null || value === undefined || value === "" ? "-" : value;
}

function routeLine(route) {
  const audit = route.auditActionCandidate ? `, audit: \`${route.auditActionCandidate}\`` : "";
  return bullet(`\`${route.method} ${route.path}\` -> permission: \`${route.permission}\`, operation: \`${route.operationId}\`${audit}`);
}

function candidateLine(file) {
  const marker = file.generated ? ", generated marker present" : "";
  return bullet(`\`${file.path}\` -> role: \`${file.role}\`, target: \`${file.eventualRuntimeTarget}\`, status: \`${file.status}\`${marker}`);
}

function resourceSection(resource) {
  const routes = resource.backend.routes.map(routeLine).join("\n");
  const auditActions = resource.audit.actionCandidates.length > 0 ? resource.audit.actionCandidates.map((action) => `\`${action}\``).join(", ") : "-";
  const resourcePlan = planByResource.get(resource.resource);
  const candidateFiles = resourcePlan?.candidateFiles ?? [];
  const candidateFileLines = candidateFiles.length > 0 ? candidateFiles.map(candidateLine).join("\n") : "-";
  return `## ${resource.resource} (${resource.label.zh ?? resource.label.en})

- Code: \`${resource.code}\`
- Group: \`${resource.group}\`
- Codegen mode: \`${resource.codegenMode}\`
- Generation level: \`${resource.generationLevel}\`
- Dry-run status: \`${resourcePlan?.dryRunStatus ?? "missing"}\`
- Source writing: \`${resourcePlan?.sourceWriting ?? "disabled"}\`

### Backend

- Model: \`${nullable(resource.backend.model)}\`
- Table: \`${nullable(resource.backend.table)}\`
- API base: \`${resource.backend.apiBase}\`
- Route file: \`${resource.backend.routeFile}\`
- Repository package: \`${resource.backend.repositoryPackage}\`

Routes:

${routes}

Audit candidates: ${auditActions}

### Frontend

- Refine resource: \`${resource.frontend.resourceName}\`
- Admin path: \`${resource.frontend.adminPath}\`
- Component: \`${resource.frontend.component}\`
- Data provider file: \`${resource.frontend.dataProviderFile}\`

### Schema

- Field count: ${resource.schema.fieldCount}
- Search: ${resource.schema.search.map((item) => `\`${item}\``).join(", ") || "-"}
- Filter: ${(resource.schema.filter ?? []).map((item) => `\`${item}\``).join(", ") || "-"}
- Sort: ${(resource.schema.sort ?? []).map((item) => `\`${item}\``).join(", ") || "-"}
- Table: ${resource.schema.table.map((item) => `\`${item}\``).join(", ") || "-"}
- Form: ${resource.schema.form.map((item) => `\`${item}\``).join(", ") || "-"}
- Localized fields: ${(resource.schema.localizedFields ?? []).map((item) => `\`${item}\``).join(", ") || "-"}

### Dry-run Candidate Files

${candidateFileLines}
`;
}

const summary = preview.summary ?? {};
const planSummary = plan.summary ?? {};
const output = `# Admin Scaffold Draft

Generated by \`scripts/generate-admin-scaffold-draft.mjs\` from \`${preview.source}\`, \`${plan.source}\` and \`${relativePlanPath}\`.

This file is a review artifact for AI-assisted development. It must not be copied blindly into runtime source files.

## Summary

- Source version: \`${preview.sourceVersion}\`
- Resource count: ${preview.resourceCount}
- Route count: ${summary.routeCount ?? 0}
- Schema count: ${summary.schemaCount ?? 0}
- Template candidates: ${(summary.templateCandidates ?? []).map((item) => `\`${item}\``).join(", ")}
- Manual review: ${(summary.manualReview ?? []).map((item) => `\`${item}\``).join(", ")}
- Write resources: ${(summary.writeResources ?? []).map((item) => `\`${item}\``).join(", ")}

## Dry-run Safety Plan

- Source writing: \`${plan.mode?.sourceWriting ?? "disabled"}\`
- Dry-run: \`${String(plan.mode?.dryRun ?? true)}\`
- Conflict detection: \`${plan.mode?.conflictDetection ?? "enabled"}\`
- Allowed write roots: ${(plan.mode?.allowedWriteRoots ?? []).map((item) => `\`${item}\``).join(", ") || "-"}
- Candidate file count: ${planSummary.candidateFileCount ?? 0}
- Conflict count: ${planSummary.conflictCount ?? 0}
- Unsafe path count: ${planSummary.unsafePathCount ?? 0}

## Guardrails

${(preview.guardrails ?? []).map(bullet).join("\n")}
${(plan.guardrails ?? []).map(bullet).join("\n")}
- Generated write routes must be implemented with failing tests before source changes are accepted.
- This draft is regenerated, not manually edited.

${resources.map(resourceSection).join("\n")}
`;

if (process.argv.includes("--stdout")) {
  process.stdout.write(output);
} else {
  fs.mkdirSync(generatedDir, { recursive: true });
  fs.writeFileSync(generatedPath, output);
  console.log(`Generated ${path.relative(repoRoot, generatedPath)}`);
}
