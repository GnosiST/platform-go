import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const previewPath = path.join(repoRoot, "resources", "generated", "admin-codegen-preview.json");
const planPath = path.join(repoRoot, "resources", "generated", "admin-scaffold-plan.json");
const generatedDir = path.join(repoRoot, "resources", "generated");
const generatedPath = path.join(generatedDir, "admin-scaffold-files.json");
const scaffoldRoot = "resources/generated/scaffold";

const preview = JSON.parse(fs.readFileSync(previewPath, "utf8"));
const plan = JSON.parse(fs.readFileSync(planPath, "utf8"));
const previewByResource = new Map((preview.resources ?? []).map((resource) => [resource.resource, resource]));
const generatedMarker = plan.mode?.generatedMarker ?? "Code generated by platform-go admin scaffold plan. DO NOT EDIT.";

function toPosix(value) {
  return value.split(path.sep).join("/");
}

function isInsideAllowedRoot(relativeFile, allowedRoot) {
  const root = path.resolve(repoRoot, allowedRoot);
  const target = path.resolve(repoRoot, relativeFile);
  const relative = path.relative(root, target);
  return relative !== "" && !relative.startsWith("..") && !path.isAbsolute(relative);
}

function sha256(content) {
  return crypto.createHash("sha256").update(content).digest("hex");
}

function commentBlock(lines) {
  return lines.map((line) => `// ${line}`).join("\n");
}

function header(file, resource) {
  return `${commentBlock([
    generatedMarker,
    `resource: ${resource.resource}`,
    `role: ${file.role}`,
    `eventual runtime target: ${file.eventualRuntimeTarget}`,
    "source writing is disabled; review this draft before any runtime source change.",
  ])}\n\n`;
}

function goPackageName(resource) {
  if (resource.backend.modelPackage.includes("/model")) return "model";
  if (resource.backend.repositoryPackage.includes("/repository")) return "repository";
  return "platform";
}

function goType(value) {
  if (!value) return "string";
  if (value === "number") return "float64";
  if (value === "switch") return "bool";
  if (value === "multiselect") return "[]string";
  return "string";
}

function exportedName(value) {
  return String(value)
    .split(/[^A-Za-z0-9]+/)
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

function renderGormModel(file, resource) {
  const fields = [
    ["ID", "string", "`gorm:\"primaryKey;size:64\" json:\"id\"`"],
    ["Code", "string", "`gorm:\"size:128;index\" json:\"code\"`"],
    ["Name", "string", "`gorm:\"size:255\" json:\"name\"`"],
    ["Status", "string", "`gorm:\"size:64;index\" json:\"status\"`"],
    ["Description", "string", "`gorm:\"type:text\" json:\"description\"`"],
    ...(resource.schema.table ?? [])
      .filter((field) => !["id", "code", "name", "status", "description", "updatedAt"].includes(field))
      .map((field) => {
        const fieldMeta = resource.schema.fields?.find((item) => item.key === field);
        return [exportedName(field), goType(fieldMeta?.type), `\`json:"${field}"\``];
      }),
    ["UpdatedAt", "time.Time", "`json:\"updatedAt\"`"],
  ];
  const body = fields.map(([name, typ, tags]) => `\t${name} ${typ} ${tags}`).join("\n");
  return `${header(file, resource)}package ${goPackageName(resource)}\n\nimport \"time\"\n\n// ${resource.backend.model} is a draft model generated from the admin resource schema.\ntype ${resource.backend.model} struct {\n${body}\n}\n`;
}

function renderRepository(file, resource) {
  return `${header(file, resource)}package repository\n\nimport \"context\"\n\n// ${exportedName(resource.resource)}Repository is a draft repository boundary for ${resource.resource}.\n// Keep runtime implementations behind platform repository ports before copying this draft.\ntype ${exportedName(resource.resource)}Repository interface {\n\tList(ctx context.Context, query ${exportedName(resource.resource)}Query) ([]${resource.backend.model ?? "Record"}, error)\n}\n\ntype ${exportedName(resource.resource)}Query struct {\n\tKeywords []string\n\tPage int\n\tPageSize int\n}\n`;
}

function renderRoute(file, resource) {
  const routes = resource.backend.routes
    .map((route) => `// ${route.method} ${route.path} -> ${route.permission}`)
    .join("\n");
  return `${header(file, resource)}package httpapi\n\n${routes}\n\n// Register${exportedName(resource.resource)}Routes is a draft route hook.\n// Prefer the generic /api/admin/resources/${resource.resource} engine unless a workflow needs custom actions.\nfunc Register${exportedName(resource.resource)}Routes() {}\n`;
}

function renderRefinePage(file, resource) {
  return `${header(file, resource)}import { GenericResourceConsole } from "../../resources/GenericResourceConsole";\n\nexport const ${exportedName(resource.frontend.resourceName)}Resource = GenericResourceConsole;\n`;
}

function renderContractTest(file, resource) {
  return `${header(file, resource)}package adminresource_test\n\nimport \"testing\"\n\nfunc Test${exportedName(resource.resource)}ResourceContract(t *testing.T) {\n\tt.Helper()\n\t// Verify ${resource.resource} permissions, schema fields and data-scope behavior before promoting this draft.\n}\n`;
}

function renderFile(file, resource) {
  if (file.role === "gormModelDraft") return renderGormModel(file, resource);
  if (file.role === "repositoryDraft") return renderRepository(file, resource);
  if (file.role === "routeDraft") return renderRoute(file, resource);
  if (file.role === "refinePageDraft") return renderRefinePage(file, resource);
  if (file.role === "contractTestDraft") return renderContractTest(file, resource);
  return `${header(file, resource)}Unsupported scaffold role ${file.role}.\n`;
}

function scaffoldFiles() {
  const allowedRoots = plan.mode?.allowedWriteRoots ?? [`${scaffoldRoot}/`];
  const files = [];
  for (const resourcePlan of plan.resources ?? []) {
    const resource = previewByResource.get(resourcePlan.resource);
    if (!resource) continue;
    for (const file of resourcePlan.candidateFiles ?? []) {
      if (file.conflict) continue;
      if (!allowedRoots.some((root) => isInsideAllowedRoot(file.path, root))) {
        continue;
      }
      const content = renderFile(file, resource);
      files.push({
        resource: resource.resource,
        role: file.role,
        path: file.path,
        eventualRuntimeTarget: file.eventualRuntimeTarget,
        sourceWriting: "disabled",
        markerPresent: content.includes(generatedMarker),
        contentSha256: sha256(content),
        preview: content.split("\n").slice(0, 8).join("\n"),
        content,
      });
    }
  }
  return files.sort((a, b) => a.path.localeCompare(b.path));
}

function manifestFor(files, includeContent) {
  return {
    generatedBy: "scripts/generate-admin-scaffold-files.mjs",
    source: "resources/generated/admin-scaffold-plan.json",
    sourceVersion: plan.sourceVersion,
    mode: {
      dryRun: true,
      sourceWriting: "disabled",
      allowedWriteRoots: plan.mode?.allowedWriteRoots ?? [`${scaffoldRoot}/`],
      generatedMarker,
    },
    summary: {
      resourceCount: new Set(files.map((file) => file.resource)).size,
      fileCount: files.length,
    },
    files: files.map((file) => ({
      resource: file.resource,
      role: file.role,
      path: file.path,
      eventualRuntimeTarget: file.eventualRuntimeTarget,
      sourceWriting: file.sourceWriting,
      markerPresent: file.markerPresent,
      contentSha256: file.contentSha256,
      preview: file.preview,
      ...(includeContent ? { content: file.content } : {}),
    })),
  };
}

const files = scaffoldFiles();
const stdoutMode = process.argv.includes("--stdout");
const manifest = manifestFor(files, false);
const output = `${JSON.stringify(manifest, null, 2)}\n`;

if (stdoutMode) {
  process.stdout.write(output);
} else {
  for (const file of files) {
    const absolutePath = path.join(repoRoot, file.path);
    fs.mkdirSync(path.dirname(absolutePath), { recursive: true });
    fs.writeFileSync(absolutePath, file.content);
  }
  fs.mkdirSync(generatedDir, { recursive: true });
  fs.writeFileSync(generatedPath, output);
  console.log(`Generated ${toPosix(path.relative(repoRoot, generatedPath))} and ${files.length} scaffold files`);
}
