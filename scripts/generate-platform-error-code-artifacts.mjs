import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { loadPlatformErrorContract, platformErrorContractRelativePath } from "./platform-error-contract.mjs";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

function argValue(name, fallback) {
  const index = process.argv.indexOf(name);
  return index === -1 ? fallback : process.argv[index + 1] ?? "";
}

const contractPath = path.resolve(repoRoot, argValue("--contract", platformErrorContractRelativePath));
const outputDir = path.resolve(repoRoot, argValue("--output-dir", "resources/generated/error-sdk"));
const contract = loadPlatformErrorContract(repoRoot, contractPath);

function identifier(value) {
  return String(value)
    .toLowerCase()
    .split(/[^a-z0-9]+/)
    .filter(Boolean)
    .map((part) => part[0].toUpperCase() + part.slice(1))
    .join("");
}

function typeScriptArtifact() {
  const union = contract.definitions.map((definition) => `  | ${JSON.stringify(definition.code)}`).join("\n");
  const definitions = Object.fromEntries(contract.definitions.map((definition) => [definition.code, definition]));
  return `// Code generated by scripts/generate-platform-error-code-artifacts.mjs; DO NOT EDIT.
// Source contract: ${contract.registrySource}
// Source hash: ${contract.contractHash}

export const platformErrorContractSource = ${JSON.stringify(contract.registrySource)} as const;
export const platformErrorContractHash = ${JSON.stringify(contract.contractHash)} as const;

export type PlatformErrorCode =
${union};

export type PlatformErrorDefinition = {
  readonly code: PlatformErrorCode;
  readonly owner: string;
  readonly planes: ReadonlyArray<string>;
  readonly audiences: ReadonlyArray<string>;
  readonly category: string;
  readonly httpStatus: number;
  readonly retryPolicy: string;
  readonly redactionClass: string;
  readonly publicMessage: string;
  readonly introducedIn: string;
  readonly deprecated: boolean;
};

export type PlatformErrorBody = {
  readonly code: PlatformErrorCode;
  readonly message: string;
  readonly requestId: string;
  readonly traceId: string;
};

export const platformErrorDefinitions = Object.freeze(${JSON.stringify(definitions, null, 2)}) as Readonly<Record<PlatformErrorCode, PlatformErrorDefinition>>;

export function isPlatformErrorCode(value: unknown): value is PlatformErrorCode {
  return typeof value === "string" && Object.prototype.hasOwnProperty.call(platformErrorDefinitions, value);
}
`;
}

function goStringSlice(values) {
  return `[]string{${values.map((value) => JSON.stringify(value)).join(", ")}}`;
}

function goArtifact() {
  const constants = contract.definitions
    .map((definition) => `\tCode${identifier(definition.code)} Code = ${JSON.stringify(definition.code)}`)
    .join("\n");
  const definitions = contract.definitions
    .map((definition) => `\t{Code: Code${identifier(definition.code)}, Owner: ${JSON.stringify(definition.owner)}, Planes: ${goStringSlice(definition.planes)}, Audiences: ${goStringSlice(definition.audiences)}, Category: ${JSON.stringify(definition.category)}, HTTPStatus: ${definition.httpStatus}, RetryPolicy: ${JSON.stringify(definition.retryPolicy)}, RedactionClass: ${JSON.stringify(definition.redactionClass)}, PublicMessage: ${JSON.stringify(definition.publicMessage)}, IntroducedIn: ${JSON.stringify(definition.introducedIn)}, Deprecated: ${definition.deprecated}},`)
    .join("\n");
  return `// Code generated by scripts/generate-platform-error-code-artifacts.mjs; DO NOT EDIT.
// Source contract: ${contract.registrySource}
// Source hash: ${contract.contractHash}

package errorcontract

const (
\tSourceContract = ${JSON.stringify(contract.registrySource)}
\tSourceHash = ${JSON.stringify(contract.contractHash)}
)

type Code string

const (
${constants}
)

type Definition struct {
\tCode Code \`json:"code"\`
\tOwner string \`json:"owner"\`
\tPlanes []string \`json:"planes"\`
\tAudiences []string \`json:"audiences"\`
\tCategory string \`json:"category"\`
\tHTTPStatus int \`json:"httpStatus"\`
\tRetryPolicy string \`json:"retryPolicy"\`
\tRedactionClass string \`json:"redactionClass"\`
\tPublicMessage string \`json:"publicMessage"\`
\tIntroducedIn string \`json:"introducedIn"\`
\tDeprecated bool \`json:"deprecated"\`
}

var definitions = []Definition{
${definitions}
}

func cloneDefinition(definition Definition) Definition {
\tdefinition.Planes = append([]string(nil), definition.Planes...)
\tdefinition.Audiences = append([]string(nil), definition.Audiences...)
\treturn definition
}

func Definitions() []Definition {
\tresult := make([]Definition, len(definitions))
\tfor index, definition := range definitions {
\t\tresult[index] = cloneDefinition(definition)
\t}
\treturn result
}

func Lookup(code Code) (Definition, bool) {
\tfor _, definition := range definitions {
\t\tif definition.Code == code {
\t\t\treturn cloneDefinition(definition), true
\t\t}
\t}
\treturn Definition{}, false
}
`;
}

function write(relativePath, source) {
  const target = path.join(outputDir, relativePath);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, source);
  return path.relative(repoRoot, target);
}

const outputs = [
  write("go/error_contract.go", goArtifact()),
  write("typescript/errorContract.ts", typeScriptArtifact()),
];

console.log(`Generated ${outputs.join(", ")}`);
